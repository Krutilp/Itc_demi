// write a program in c to find total of two integer.
#include<stdio.h>
int main()
{
	// variable declaration
	int no1,no2,total,avg;
	// initialization
	no1=30;
	no2=20;
	//logic
	total=no1+no2;
	avg=total/2;
	//output
	printf("total of %d and %d is %d\n",no1,no2,total);
	printf("avg of %d and %d is %d",no1,no2,avg);
	
	return 0;
}
